import Task from "./Task";

export default function Tasks({ tasks, handleTaskClick, handleTaskDeletion }) {

  return (
    <>
      {tasks.map((task) => (
        <Task
          key={task.id}
          task={task}
          handleTaskClick={handleTaskClick}
          handleTaskDeletion={handleTaskDeletion}
        />
      ))}
    </>
  )
}